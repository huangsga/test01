package com.infotools.system.Entity;

public class DoctorInfo {
    private Integer doc_id;
    private String doc_category;
    private String doc_hospital;
    private String doc_name;
    private String doc_awards;
    private String doc_details;
    private String doc_title;
    private String doc_img_url;

    public Integer getDoc_id() {
        return doc_id;
    }

    public void setDoc_id(Integer doc_id) {
        this.doc_id = doc_id;
    }

    public String getDoc_category() {
        return doc_category;
    }

    public void setDoc_category(String doc_category) {
        this.doc_category = doc_category;
    }

    public String getDoc_hospital() {
        return doc_hospital;
    }

    public void setDoc_hospital(String doc_hospital) {
        this.doc_hospital = doc_hospital;
    }

    public String getDoc_name() {
        return doc_name;
    }

    public void setDoc_name(String doc_name) {
        this.doc_name = doc_name;
    }

    public String getDoc_awards() {
        return doc_awards;
    }

    public void setDoc_awards(String doc_awards) {
        this.doc_awards = doc_awards;
    }

    public String getDoc_details() {
        return doc_details;
    }

    public void setDoc_details(String doc_details) {
        this.doc_details = doc_details;
    }

    public String getDoc_title() {
        return doc_title;
    }

    public void setDoc_title(String doc_title) {
        this.doc_title = doc_title;
    }

    public String getDoc_img_url() {
        return doc_img_url;
    }

    public void setDoc_img_url(String doc_img_url) {
        this.doc_img_url = doc_img_url;
    }
}
