package com.infotools.system.Entity;

public class DetailTime {
    private Integer looper_id;
    private String ill_name;
    private String ill_stage;
    private String ill_introduce;

    public Integer getLooper_id() {
        return looper_id;
    }

    public void setLooper_id(Integer looper_id) {
        this.looper_id = looper_id;
    }

    public String getIll_name() {
        return ill_name;
    }

    public void setIll_name(String ill_name) {
        this.ill_name = ill_name;
    }

    public String getIll_stage() {
        return ill_stage;
    }

    public void setIll_stage(String ill_stage) {
        this.ill_stage = ill_stage;
    }

    public String getIll_introduce() {
        return ill_introduce;
    }

    public void setIll_introduce(String ill_introduce) {
        this.ill_introduce = ill_introduce;
    }
}
